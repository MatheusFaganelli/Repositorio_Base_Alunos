bombons = 10 # contador, variavel que inicia a contagem

while bombons > 0: #enquanto bombons >0 o while continua repetindo o codigo
    print(f"eu tenho {bombons} bombons.")  
    bombons -= 1 # diminui um bombom
    print(f"comi 1 e fiquei com {bombons} bombons") # informa a quantidade 
    # de bombons depois o -
    if bombons == 0: # quando a quantidade de bombons for 0
        print("acabaram os bombons.") # informa que acabaram os bomboms